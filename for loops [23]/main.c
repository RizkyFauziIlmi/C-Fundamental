#include <stdio.h>

int main()
{
    int i; // declare variable i

    // for loop to print numbers 0-9
    for (i = 0; i < 10; i++)
    {                      // initialize i to 0; loop while i < 10; increment i by 1 after each iteration
        printf("%d\n", i); // print the value of i to the screen
    }

    return 0; // return 0 to indicate successful program completion
}
